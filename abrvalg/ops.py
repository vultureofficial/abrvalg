

def add(l, r):
    return  str(l) + " + " + str(r) 

def sub(l, r):
    return  str(l) + " - " + str(r) 

def mul(l, r):
    return  str(l) + " * " + str(r) 

def div(l, r):
    return  str(l) + " / " + str(r) 

def mod(l, r):
    return  str(l) + " % " + str(r) 

def gt(l, r):
    return  str(l) + " > " + str(r) 

def lt(l, r):
    return  str(l) + " < " + str(r) 
def ge(l, r):
    return  str(l) + " >= " + str(r) 

def le(l, r):
    return  str(l) + " <= " + str(r) 

def eq(l, r):
    return  str(l) + " == " + str(r) 

def ne(l, r):
    return  str(l) + " != " + str(r)